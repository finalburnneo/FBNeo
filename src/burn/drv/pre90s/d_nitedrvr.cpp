static INT32 DrvDoReset(INT32 clear_mem)
{
	if (clear_mem) {
		memset (AllRam, 0, RamEnd - AllRam);
	}

	M6502Open(0);
	M6502Reset();
	BurnSampleReset();
	BurnSamplePlay(0); // engine
	M6502Close();

	BurnWatchdogReset();

	DrvPalette[1] = ~0; // white

	crash_en = 0;
	crash_data_en = 0;
	crash_data = 0;
	crash_timer = 0;
	steering_buf = 0;
	ac_line = 0;
	m_track = 0;
	steering_val = 0;
	last_steering_val = 0;
	m_gear = 1;
	last = 0;

	sound_disable = 1;

	HiscoreReset();

	return 0;
}

static INT32 MemIndex()
{
	UINT8 *Next; Next = AllMem;

	DrvM6502ROM		= Next; Next += 0x001000;

	DrvGfxROM		= Next; Next += 0x001000*4;
	DrvCarBitmap	= Next; Next += 14080;

	AllRam			= Next;

	DrvPalette		= (UINT32*)Next; Next += 0x0012 * sizeof(UINT32);

	DrvM6502RAM		= Next; Next += 0x000100;
	DrvVidRAM		= Next; Next += 0x000080;
	DrvHVCRAM		= Next; Next += 0x000080;

	RamEnd			= Next;

	MemEnd			= Next;

	return 0;
}

