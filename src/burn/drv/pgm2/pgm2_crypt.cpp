// pgm2_crypt.cpp - IGS036 (PGM2) ROM decryption for FBNeo
//
// IGS036 uses a substitution cipher on 16-bit halfwords.  Each game has a
// unique 256-byte key table that is provided by the internal ARM9 boot ROM
// at runtime (written to 0xFFFFFC00-0xFFFFFCFF before decryption fires).
//
// Algorithm derived from MAME src/mame/igs/igs036crypt.cpp:
//   1. Address-dependent rotation: ROL16(cipherword, rotation(word_addr))
//   2. Fixed global bitswap: bitswap<16>(x, 10,9,8,7,0,15,6,5,14,13,4,3,12,11,2,1)
//   3. Key-dependent XOR via 16 trigger conditions from a 32-bit trigger table
//
// The key tables stored below are now UNUSED (runtime decryption handles them)
// but kept for reference.  See MAME license for original authorship.

#include "pgm2.h"
#include "pgm2_crypt.h"

