// ---------------------------------------------------------------------------
// IGS036 decryption engine — matches MAME src/mame/igs/igs036crypt.cpp
// ---------------------------------------------------------------------------

static inline int pg2_bit(int x, int n) { return (x >> n) & 1; }

// Fixed global bitswap applied after rotation:
// bitswap<16>(x, 10,9,8,7,0,15,6,5, 14,13,4,3,12,11,2,1)
static UINT16 pg2_deobf_bitswap(UINT16 x)
{
    return (UINT16)(
          (pg2_bit(x, 10) << 15) | (pg2_bit(x,  9) << 14)
        | (pg2_bit(x,  8) << 13) | (pg2_bit(x,  7) << 12)
        | (pg2_bit(x,  0) << 11) | (pg2_bit(x, 15) << 10)
        | (pg2_bit(x,  6) <<  9) | (pg2_bit(x,  5) <<  8)
        | (pg2_bit(x, 14) <<  7) | (pg2_bit(x, 13) <<  6)
        | (pg2_bit(x,  4) <<  5) | (pg2_bit(x,  3) <<  4)
        | (pg2_bit(x, 12) <<  3) | (pg2_bit(x, 11) <<  2)
        | (pg2_bit(x,  2) <<  1) | (pg2_bit(x,  1) <<  0));
}

static UINT16 pg2_rol16(UINT16 v, int n)
{
    n &= 0xf;
    return (UINT16)((v << n) | (v >> ((16 - n) & 0xf)));
}

// rot_enabling[group_bit][col] — MAME uses function-pointer table
// col  = (addr ^ (0x1b * BIT(addr,2))) & 3
// Match MAME's table-driven boolean function implementation exactly.
typedef int (*pg2_bool_fn)(int);

static int pg2_unknown(int) { return 0; }
static int pg2_czero(int) { return 0; }
static int pg2_cone(int) { return 1; }
static int pg2_bit_3(int address) { return pg2_bit(address, 3); }
static int pg2_bit_4(int address) { return pg2_bit(address, 4); }
static int pg2_bit_7(int address) { return pg2_bit(address, 7); }
static int pg2_not_3(int address) { return pg2_bit(address, 3) ^ 1; }
static int pg2_not_4(int address) { return pg2_bit(address, 4) ^ 1; }
static int pg2_not_7(int address) { return pg2_bit(address, 7) ^ 1; }
static int pg2_xor_37(int address) { return pg2_bit(address, 3) ^ pg2_bit(address, 7); }
static int pg2_xnor_37(int address) { return pg2_bit(address, 3) ^ pg2_bit(address, 7) ^ 1; }
static int pg2_xor_47(int address) { return pg2_bit(address, 4) ^ pg2_bit(address, 7); }
static int pg2_xnor_47(int address) { return pg2_bit(address, 4) ^ pg2_bit(address, 7) ^ 1; }
static int pg2_nor_34(int address) { return (pg2_bit(address, 3) | pg2_bit(address, 4)) ^ 1; }
static int pg2_impl_43(int address) { return pg2_bit(address, 3) || (pg2_bit(address, 4) ^ 1); }

