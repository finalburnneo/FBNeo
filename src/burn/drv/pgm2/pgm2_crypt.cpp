static pg2_bool_fn pg2_rot_direction[4][8] = {
    {pg2_bit_3, pg2_xor_37 , pg2_xnor_37, pg2_not_3, pg2_bit_3, pg2_xor_37 , pg2_xnor_37, pg2_not_3},
    {pg2_czero, pg2_not_7  , pg2_not_7  , pg2_czero, pg2_czero, pg2_not_7  , pg2_not_7  , pg2_czero},
    {pg2_bit_4, pg2_xor_47 , pg2_xnor_47, pg2_not_4, pg2_bit_4, pg2_xor_47 , pg2_xnor_47, pg2_not_4},
    {pg2_bit_3, pg2_not_7  , pg2_bit_7  , pg2_czero, pg2_cone,  pg2_not_7  , pg2_bit_7  , pg2_czero},
};

static int pg2_rot_enabled(int addr, const int *grp)
{
    for (int j = 0; j < 4; j++) {
        if (pg2_bit(addr, 8 + grp[j])) {
            int aux = addr ^ (0x1b * pg2_bit(addr, 2));
            return pg2_rot_enabling[grp[j]][aux & 3](aux);
        }
    }
    return 0;
}

static int pg2_rot_group(int addr, const int *grp)
{
    // MAME: rot_direction[group[0] & 3][address & 7](address)
    int aux = pg2_rot_direction[grp[0] & 3][addr & 7](addr);
    return (aux * 2) - 1;
}

static int pg2_rotation(int wa)
{
    static const int g15[] = {15, 11, 7, 5};
    static const int g14[] = {14,  9, 3, 2};
    static const int g13[] = {13, 10, 6, 1};
    static const int g12[] = {12,  8, 4, 0};

    int en0 = pg2_rot_enabled(wa, g15);
    int rot = en0 * pg2_rot_group(wa, g15) * 9;

    int en1 = en0 ^ pg2_rot_enabled(wa, g14);
    rot += en1 * pg2_rot_group(wa, g14) * 1;

    int en2 = en0 ^ pg2_rot_enabled(wa, g13);
    rot += en2 * pg2_rot_group(wa, g13) * 2;

    int en3 = en0 ^ pg2_rot_enabled(wa, g12);
    rot += en3 * pg2_rot_group(wa, g12) * 4;

    // Block-independent rotation (lowest 8 bits only)
    int b0 = pg2_bit(wa, 0), b1 = pg2_bit(wa, 1),
        b3 = pg2_bit(wa, 3), b4 = pg2_bit(wa, 4), b7 = pg2_bit(wa, 7);
    int rot2  = 4 * b0;
    rot2 += 1 * b4 * (b0 * 2 - 1);
    rot2 += 4 * b3 * (b0 * 2 - 1);
    rot2 *= (b7 | (b0 ^ b1 ^ 1)) * 2 - 1;
    rot2 += 2 * ((b0 ^ b1) & (b7 ^ 1));

    return (rot + rot2) & 0xf;
}

// 16 trigger pairs controlling which output bit is XORed with key vs. constant
static const UINT32 pg2_triggers[16][2] = {
    {0x000101, 0x000001}, {0x000802, 0x000800}, {0x000204, 0x000004}, {0x000408, 0x000408},
    {0x010010, 0x000010}, {0x020020, 0x000020}, {0x040040, 0x000040}, {0x080080, 0x080080},
    {0x100100, 0x000100}, {0x200200, 0x200000}, {0x400400, 0x400000}, {0x800801, 0x000001},
    {0x001004, 0x001000}, {0x002010, 0x002000}, {0x004040, 0x000040}, {0x008100, 0x008100}
};

static UINT16 pg2_decrypt_hw(UINT16 cipher, int word_addr, const UINT8 *key)
{
    // Step 1: key-independent deobfuscation
    int aux = pg2_deobf_bitswap(pg2_rol16(cipher, pg2_rotation(word_addr)));

    // Step 2: key-dependent XOR per bit
    UINT8 k = key[word_addr & 0xff];
    for (int i = 0; i < 16; i++) {
        int bit = ((word_addr & pg2_triggers[i][0]) == pg2_triggers[i][1])
                  ? pg2_bit(k, i & 7)
                  : pg2_bit(0x1a3a, i);
        aux ^= (bit << i);
    }
    return (UINT16)aux;
}

// ---------------------------------------------------------------------------
// Public interface — decrypt ROM buffer in-place using 256-byte key table
// ---------------------------------------------------------------------------
void pgm2_igs036_decrypt(UINT8 *rom, UINT32 romlen, const UINT8 *key, INT32 offset)
{
    UINT16 *data   = (UINT16 *)rom;
    UINT32  nwords = romlen / 2;

    for (UINT32 i = 0; i < nwords; i++) {
        UINT16 val = BURN_ENDIAN_SWAP_INT16(data[i]);
        val = pg2_decrypt_hw(val, (int)(i + offset), key);
        data[i] = BURN_ENDIAN_SWAP_INT16(val);
    }
}

// MAME's bitswap<16>(v, 8,9,10,11,12,13,14,15,0,1,2,3,4,5,6,7)
// swaps the two bytes AND reverses bit order within each byte
static inline UINT16 pgm2_mask_bitswap(UINT16 v)
{
    return (UINT16)(
        (pg2_bit(v, 8) << 15) | (pg2_bit(v, 9) << 14) | (pg2_bit(v, 10) << 13) | (pg2_bit(v, 11) << 12) |
        (pg2_bit(v, 12) << 11) | (pg2_bit(v, 13) << 10) | (pg2_bit(v, 14) << 9) | (pg2_bit(v, 15) << 8) |
        (pg2_bit(v, 0) << 7) | (pg2_bit(v, 1) << 6) | (pg2_bit(v, 2) << 5) | (pg2_bit(v, 3) << 4) |
        (pg2_bit(v, 4) << 3) | (pg2_bit(v, 5) << 2) | (pg2_bit(v, 6) << 1) | (pg2_bit(v, 7) << 0));
}

static void pgm2_iga_u16_decode(UINT16 *rom, INT32 len, UINT16 ixor)
{
    for (INT32 i = 1; i < (len / 2); i += 2) {
        UINT16 x = ixor;
        INT32 t = (i >> 1);

        if (t & 0x000001) x ^= 0x0010;
        if (t & 0x000002) x ^= 0x2004;
        if (t & 0x000004) x ^= 0x0801;
        if (t & 0x000008) x ^= 0x0300;
        if (t & 0x000010) x ^= 0x0080;
        if (t & 0x000020) x ^= 0x0020;
        if (t & 0x000040) x ^= 0x4008;
        if (t & 0x000080) x ^= 0x1002;
        if (t & 0x000100) x ^= 0x0400;
        if (t & 0x000200) x ^= 0x0040;
        if (t & 0x000400) x ^= 0x8000;

        UINT16 v = BURN_ENDIAN_SWAP_INT16(rom[i]);
        v ^= x;
        v = pgm2_mask_bitswap(v);
        rom[i] = BURN_ENDIAN_SWAP_INT16(v);
    }
}

static void pgm2_iga_u12_decode(UINT16 *rom, INT32 len, UINT16 ixor)
{
    for (INT32 i = 0; i < (len / 2); i += 2) {
        UINT16 x = ixor;
        INT32 t = (i >> 1);

        if (t & 0x000001) x ^= 0x9004;
        if (t & 0x000002) x ^= 0x0028;
        if (t & 0x000004) x ^= 0x0182;
        if (t & 0x000008) x ^= 0x0010;
        if (t & 0x000010) x ^= 0x2040;
        if (t & 0x000020) x ^= 0x0801;
        if (t & 0x000100) x ^= 0x4000;
        if (t & 0x000200) x ^= 0x0600;

        UINT16 v = BURN_ENDIAN_SWAP_INT16(rom[i]);
        v ^= x;
        v = pgm2_mask_bitswap(v);
        rom[i] = BURN_ENDIAN_SWAP_INT16(v);
    }
}

